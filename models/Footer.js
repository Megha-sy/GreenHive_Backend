const mongoose = require("mongoose");

const footerSchema = new mongoose.Schema(
  {
    about: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    location: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("Footer", footerSchema);
